function getStatusColor(bl, warn, danger) {
	if (bl < danger) {
		return 'danger';
	}
	if (bl < warn) {
		return 'warning';
	}
	if (bl >= warn) {
		return 'ok';
	}
}

//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
function getChargerStatus(state, scheduled) {

	if (state == "Stopped") {
		if (scheduled) {
			return 'Scheduled';
		} else {
			return 'Stopped';
		}
	} else {
		return state; 
	}

}

